package MethodReference;

public class Student {
    private  int RollNo;
    private String Name;
    public Student(){

    }
    public int getRollNo() {
        return RollNo;
    }

    public void setRollNo(int rollNo) {
        RollNo = rollNo;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }
}
